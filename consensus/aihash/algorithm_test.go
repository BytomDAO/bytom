// convert []byte to []*bc.Hash
func bytesToSlicePointerHash(src []byte) []*bc.Hash {
	var sbh []*bc.Hash
	for i := 0; i < len(src)/32; i++ {
		s := src[i*32 : (i+1)*32]
		tmp := bc.BytesToHash(s)
		sbh = append(sbh, &tmp)
	}

	return sbh
}
